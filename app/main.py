@app.post("/api/jira/{issue_key}/approve-triage", response_model=ApprovedTriageResult)
def approve_jira_triage(
    issue_key: str,
    confirmation: PublishConfirmation,
    request: Request,
) -> ApprovedTriageResult:
    try:
        current_issue = get_jira_issue(issue_key)
    except JiraConfigurationError as exc:
        raise HTTPException(status_code=500, detail=str(exc)) from exc
    except JiraRequestError as exc:
        raise HTTPException(status_code=502, detail=str(exc)) from exc
    if "ai-triaged" in current_issue.get("labels", []):
        raise HTTPException(status_code=409, detail="This issue has already been triaged.")

    decision = triage_jira_issue(issue_key, request)
    preview = build_jira_preview(decision)
    labels = [
        "ai-triaged",
        f"route-{label_slug(decision.category)}",
        f"team-{label_slug(decision.assigned_team_name.replace('KRkRice IT ', ''))}",
    ]
    try:
        comment_id = add_internal_comment(preview.issue_key, preview.comment)
        added = add_labels(preview.issue_key, labels)
        assign_team(preview.issue_key, decision.assigned_team_id)
    except JiraConfigurationError as exc:
        raise HTTPException(status_code=500, detail=str(exc)) from exc
    except JiraRequestError as exc:
        raise HTTPException(status_code=502, detail=str(exc)) from exc
    record_approved_triage(
        issue_key=preview.issue_key,
        action=preview.action,
        category=decision.category,
        assigned_team_id=decision.assigned_team_id,
        assigned_team_name=decision.assigned_team_name,
        comment_id=comment_id,
        labels=added,
    )
    return ApprovedTriageResult(
        issue_key=preview.issue_key,
        action=preview.action,
        comment_id=comment_id,
        labels_added=added,
    )


def execute_autonomous_triage(issue_key: str, request: Request) -> AutonomousTriageResult:
    current_issue = get_jira_issue(issue_key)
    if "ai-triaged" in current_issue.get("labels", []):
        raise HTTPException(status_code=409, detail="This issue has already been triaged.")

    decision = triage_jira_issue(issue_key, request)
    preview = build_jira_preview(decision)
    labels = [
        "ai-triaged",
        "ai-autonomous",
        f"route-{label_slug(decision.category)}",
        f"team-{label_slug(decision.assigned_team_name.replace('KRkRice IT ', ''))}",
        f"decision-{decision.decision}",
    ]
    if decision.decision == "respond":
        resolution = (decision.suggested_response or "").split(": ", 1)[-1]
        comment = (
            "Hello,\n\n"
            f"Our IT helpdesk agent found an approved solution: {resolution}\n\n"
            "If this does not resolve the problem, reply to this ticket and the IT team will investigate.\n\n"
            "— KRkRice IT Helpdesk"
        )
        action = "add_public_comment"
        comment_id = add_public_comment(preview.issue_key, comment)
    else:
        action = preview.action
        comment = preview.comment.replace("Human review required.", "Published autonomously; available for review.")
        comment_id = add_internal_comment(preview.issue_key, comment)
    added = add_labels(preview.issue_key, labels)
    assign_team(preview.issue_key, decision.assigned_team_id)
    record_triage(
        issue_key=preview.issue_key,
        action=action,
        category=decision.category,
        assigned_team_id=decision.assigned_team_id,
        assigned_team_name=decision.assigned_team_name,
        comment_id=comment_id,
        labels=added,
        event_type="autonomous_triage",
    )
    return AutonomousTriageResult(
        issue_key=preview.issue_key,
        action=action,
        decision=decision.decision,
        confidence=decision.confidence,
        comment_id=comment_id,
        labels_added=added,
    )


@app.post("/api/jira/autonomous-run", response_model=list[AutonomousTriageResult])
def autonomous_jira_run(
    confirmation: AutonomousConfirmation,
    request: Request,
    limit: int = 10,
) -> list[AutonomousTriageResult]:
    try:
        issues = list_pending_issues(limit)
        return [execute_autonomous_triage(issue["issue_key"], request) for issue in issues]
    except JiraConfigurationError as exc:
        raise HTTPException(status_code=500, detail=str(exc)) from exc
    except JiraRequestError as exc:
        raise HTTPException(status_code=502, detail=str(exc)) from exc


@app.get("/api/audit", response_model=list[AuditEvent])
def audit_events(limit: int = 50) -> list[AuditEvent]:
    return [AuditEvent(**event) for event in list_audit_events(limit)]
