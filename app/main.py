from contextlib import asynccontextmanager
from pathlib import Path

from fastapi import FastAPI, HTTPException, Request
from fastapi.responses import FileResponse
from dotenv import load_dotenv

from app.audit import init_audit_db, list_audit_events, record_approved_triage, record_triage
from app.jira import JiraConfigurationError, JiraRequestError, add_internal_comment, add_labels, add_public_comment, assign_team, get_jira_issue, list_pending_issues
from app.repository import load_dataset
from app.schemas import AutonomousConfirmation, AutonomousTriageResult, ApprovedTriageResult, AuditEvent, JiraCommentPreview, JiraCommentResult, JiraRoutingResult, PendingJiraIssue, PublishConfirmation, TicketInput, TriageDecision
from app.triage import make_decision


load_dotenv()
STATIC_DIR = Path(__file__).resolve().parent / "static"


@asynccontextmanager
async def lifespan(app: FastAPI):
    init_audit_db()
    app.state.dataset = load_dataset()
    yield


app = FastAPI(
    title="KRkRice Helpdesk Agent",
    version="0.1.0",
    description="Safe, synthetic IT helpdesk triage MVP.",
    lifespan=lifespan,
)


@app.get("/", include_in_schema=False)
def dashboard() -> FileResponse:
    return FileResponse(STATIC_DIR / "dashboard.html")


@app.get("/health")
def health(request: Request) -> dict[str, object]:
    dataset = request.app.state.dataset
    return {
        "status": "ok",
        "records": {name: len(rows) for name, rows in dataset.items()},
    }


@app.post("/api/triage", response_model=TriageDecision)
def triage(ticket: TicketInput, request: Request) -> TriageDecision:
    return make_decision(ticket, request.app.state.dataset)


@app.post("/api/jira/{issue_key}/triage", response_model=TriageDecision)
def triage_jira_issue(issue_key: str, request: Request) -> TriageDecision:
    try:
        issue = get_jira_issue(issue_key)
    except JiraConfigurationError as exc:
        raise HTTPException(status_code=500, detail=str(exc)) from exc
    except JiraRequestError as exc:
        raise HTTPException(status_code=502, detail=str(exc)) from exc

    ticket = TicketInput(
        issue_key=issue["issue_key"],
        summary=issue["summary"],
        description=issue["description"],
        use_ai=True,
    )
    return make_decision(ticket, request.app.state.dataset)


def build_jira_preview(decision: TriageDecision) -> JiraCommentPreview:
    approved_sources = [match.record_id for match in decision.knowledge_matches if match.score >= 0.30]
    sources = ", ".join(approved_sources[:2]) or "None"

    if decision.decision == "respond":
        action = "add_internal_comment"
        comment = (
            "## AI triage preview\n\n"
            f"**Category:** {decision.category}\n"
            f"**Suggested team:** {decision.assigned_team_name}\n"
            f"**Confidence:** {decision.confidence:.0%}\n"
            f"**Knowledge sources:** {sources}\n\n"
            f"{decision.suggested_response}\n\n"
            "_Generated by the KRkRice helpdesk agent. Human review required._"
        )
    elif decision.decision == "escalate":
        action = "escalate"
        comment = (
            "## AI triage preview — escalation required\n\n"
            f"**Category:** {decision.category}\n"
            f"**Team:** {decision.assigned_team_name}\n"
            f"**Reason:** {decision.escalation_reason}\n\n"
            "_No automatic response was generated._"
        )
    else:
        action = "route_only"
        comment = (
            "## AI triage preview — routing only\n\n"
            f"**Category:** {decision.category}\n"
            f"**Suggested team:** {decision.assigned_team_name}\n"
            f"**Reason:** {decision.escalation_reason}\n\n"
            "_No sufficiently strong approved answer was found._"
        )

    return JiraCommentPreview(
        issue_key=decision.issue_key,
        action=action,
        comment=comment,
        will_write_to_jira=False,
    )


@app.post("/api/jira/{issue_key}/preview", response_model=JiraCommentPreview)
def preview_jira_action(issue_key: str, request: Request) -> JiraCommentPreview:
    return build_jira_preview(triage_jira_issue(issue_key, request))


@app.post("/api/jira/{issue_key}/publish-internal-comment", response_model=JiraCommentResult)
def publish_internal_comment(
    issue_key: str,
    confirmation: PublishConfirmation,
    request: Request,
) -> JiraCommentResult:
    # Pydantic enforces the explicit PUBLISH confirmation before this function runs.
    preview = preview_jira_action(issue_key, request)
    try:
        comment_id = add_internal_comment(preview.issue_key, preview.comment)
    except JiraConfigurationError as exc:
        raise HTTPException(status_code=500, detail=str(exc)) from exc
    except JiraRequestError as exc:
        raise HTTPException(status_code=502, detail=str(exc)) from exc
    return JiraCommentResult(issue_key=preview.issue_key, comment_id=comment_id)


def label_slug(value: str) -> str:
    return "-".join(part for part in value.lower().replace("&", " ").split() if part)


@app.post("/api/jira/{issue_key}/publish-routing-labels", response_model=JiraRoutingResult)
def publish_routing_labels(
    issue_key: str,
    confirmation: PublishConfirmation,
    request: Request,
) -> JiraRoutingResult:
    decision = triage_jira_issue(issue_key, request)
    labels = [
        "ai-triaged",
        f"route-{label_slug(decision.category)}",
        f"team-{label_slug(decision.assigned_team_name.replace('KRkRice IT ', ''))}",
    ]
    try:
        added = add_labels(decision.issue_key, labels)
        assign_team(decision.issue_key, decision.assigned_team_id)
    except JiraConfigurationError as exc:
        raise HTTPException(status_code=500, detail=str(exc)) from exc
    except JiraRequestError as exc:
        raise HTTPException(status_code=502, detail=str(exc)) from exc
    return JiraRoutingResult(issue_key=decision.issue_key, labels_added=added)


